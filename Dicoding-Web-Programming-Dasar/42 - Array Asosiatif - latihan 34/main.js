//Array Asosiatif

//Dalam Javascript array dapat dituliskan berdasarkan indeksnya dengan fitur object
var kegemaran = ['Berenang, Singkong Keju, Jus Alpukat'];

console.log(kegemaran);

var kegemaran = {
    aktivitas : 'Berenang',
    makanan : 'Singkong Keju',
    minuman : 'Jus Alpukat',
}

//Untuk Menampilkan mirip sekali dengan C++

console.log(kegemaran.aktivitas);
console.log(kegemaran.makanan);
console.log(kegemaran.minuman);